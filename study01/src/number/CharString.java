package number;

public class CharString {

	public static void main(String[] args) {
		
		System.out.println('생'); // Char(문자 작은 따옴표)
		System.out.println("생활코딩" + "입니다."); // String(문자열은 큰따옴표)
		// \ :  큰따옴표가 문자열로 인식된다.
		System.out.println("egoing said\"Welcomeprogramming worl\"");
		// \n: 줄바꿈을 해서 출력 해준다. 
		System.out.println("egoing said\n\"Welcomeprogramming worl\"");
		
		
	}

}
