package arithmetic;

public class PrePost {

	public static void main(String[] args) {
		int i = 3;
		
		i++;
		
		// 4출력
		System.out.println(i);
		
		++i;
		
		// 5출력
		System.out.println(i);
		
		// 6출력
		System.out.println(++i);
		
		// 6출력
		System.out.println(i++);
		
		// 7출력
		System.out.println(i);
		
	}

}
