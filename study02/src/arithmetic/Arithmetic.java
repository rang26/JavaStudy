package arithmetic;

public class Arithmetic {

	public static void main(String[] args) {
		// result의 값은 3
		int result = 1 + 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result - 1;
		System.out.println(result);
		
		// result의 값은 4
		result = result * 2;
		System.out.println(result);
		
		// result의 값은 2(나누기)
		result = result / 2;
		System.out.println(result);
		
		// result의 값은 10
		result = result + 8;
		System.out.println(result);
		
		// result의 값은 3 (나머지)
		result = result % 7;
		System.out.println(result);
	}

}
